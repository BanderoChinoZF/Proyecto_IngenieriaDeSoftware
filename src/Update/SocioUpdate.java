    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        try {                                              
            PreparedStatement pat = cn.prepareStatement("UPDATE socio  SET nombre=?, ap=?, am=?, fechan=?, curp=?, rfc=?, domicilio=?, ocupacion=?, telefono=?, correo=?, sexo=?, codigoPostal=? WHERE idSocio=?");

            pat.setString(1, nombre.getText());
            pat.setString(2, ap.getText());
            pat.setString(3, am.getText());
            pat.setString(4, fn.getText());
            pat.setString(5, curp.getText());
            pat.setString(6, rfc.getText());
            pat.setString(7, domicilio.getText());
            pat.setString(8, ocupacion.getText());
            pat.setString(9, telefono.getText());
            pat.setString(10,correo.getText());
            pat.setString(11, sexo.getSelectedItem().toString());
            pat.setString(12,cp.getText());
            pat.setString(13,txtid.getText());

            int res = pat.executeUpdate();

            if(res > 0)
            {//nombre,ap,am,fechan,curp,rfc,domicilio,ocupacion,telefono,correo,sexo,codigoPostal

                new rojerusan.RSNotifyFade("¡ACTUALIZADO!", "SE HA ACTUALIZADO ESTE REGISTRO",
                    3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.SUCCESS).setVisible(true);
                nombre.setText("");
                ap.setText("");
                am.setText("");
                fn.setText("");
                curp.setText("");
                domicilio.setText("");
                ocupacion.setText("");
                telefono.setText("");
                correo.setText("");
                sexo.setSelectedIndex(0);
                cp.setText("");
            }
            else
            {
                new rojerusan.RSNotifyFade("¡ERROR!", "NO SE HA PODIDO ACTUALIZAR, VERIFIQUE SUS DATOS",
                    4, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.ERROR).setVisible(true);
            }

        } catch (Exception e) {
            new rojerusan.RSNotifyFade("¡ERROR!", "",
                4, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.ERROR).setVisible(true);

        }
    }//GEN-LAST:event_btnActualizarActionPerformed

