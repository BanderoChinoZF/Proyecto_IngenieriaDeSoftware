    private void txtBusquedaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBusquedaKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()== KeyEvent.VK_ENTER)
        {
            try{

                PreparedStatement pat = cn.prepareStatement("SELECT * FROM socio WHERE  nombre= ?");
                pat.setString(1, txtBusqueda.getText());

                ResultSet rs = pat.executeQuery();
                if(rs.next()){
                    txtid.setText(rs.getString("idSocio"));
                    nombre.setText(rs.getString("nombre"));
                    ap.setText(rs.getString("ap"));
                    am.setText(rs.getString("am"));
                    fn.setText(rs.getString("fechan"));
                    curp.setText(rs.getString("curp"));
                    rfc.setText(rs.getString("rfc"));
                    domicilio.setText(rs.getString("domicilio"));
                    ocupacion.setText(rs.getString("ocupacion"));
                    telefono.setText(rs.getString("telefono"));
                    correo.setText(rs.getString("correo"));
                    sexo.setSelectedItem(rs.getString("sexo"));
                    cp.setText(rs.getString("codigoPostal"));
                } else {
                    JOptionPane.showMessageDialog(null, "No existe el registro que está buscando o verifique que este bien escrita su busqueda");

                }

            } catch(Exception e){
                System.err.println(e);
            }
        }
    }//GEN-LAST:event_txtBusquedaKeyPressed

