    private void numpolizaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_numpolizaKeyTyped
         char validar = evt.getKeyChar();
        if(Character.isLetter(validar))
        {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane,"solo se pueden ingresar nÃºmeros");
        }
           int pValor = 13;
        if (numpoliza.getText().length()>=pValor)
      {
       evt.consume();
       
                          JOptionPane.showMessageDialog(null,"limite de acaracteres excedido");
      }
    }//GEN-LAST:event_numpolizaKeyTyped

