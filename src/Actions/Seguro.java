    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

         try {
         //  int a = Integer.parseInt(id.getText());
           PreparedStatement pat = cn.prepareStatement("INSERT INTO seguro(noPoliza,beneficiario,fechaCont,vencimiento) VALUES(?,?,?,?)");
          
          // pat.setInt(1, a);
           pat.setString(1, numpoliza.getText());
           pat.setString(2, fechainicio.getText());
           pat.setString(3, vencimiento.getText());
           pat.setString(4, beneficiario.getText());
          
           pat.executeUpdate();
            new rojerusan.RSNotifyFade("Â¡AGREGADO!", "SE HA AGREGADO UN NUEVO SEGURO A LA BASE DE DATOS",
                    3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.SUCCESS).setVisible(true);
           
//           id.setText("");
           numpoliza.setText("");
           fechainicio.setText("");
           vencimiento.setText("");
           beneficiario.setText("");
          

        } catch (SQLException ex) {
             new rojerusan.RSNotifyFade("Â¡ERROR!", "NO SE HA PODIDO AGREGAR, VERIFIQUE SUS DATOS",
                    4, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.ERROR).setVisible(true);

       }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

